package Arrays.Inheritance;
class Person {
    private String firstname;
    private String lastname;
    private int age;

    public String setFirstname (){
        firstname = firstname;
        return firstname;

    }
    public String getFirstname (){
        return firstname;
    }
    public String setLastname (){
        lastname = lastname;
        return lastname;
    }
    public String getLastname(){
        return lastname;
    }
    public int setAge (){
        age = age;
        return age;
    }
    public int getAge (){
        return age;
    }
}