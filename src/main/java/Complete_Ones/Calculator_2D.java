public class Calculator_2D {
    public static void main(String[] args) {

        for (int i = 1; i < 4; i++) {
            for (int j = 0; j < 5 ; j++) {
                System.out.println("Outer: " + i + " x " + "Inner " + j + " = " + i * j);

            }

        }
    }
}
